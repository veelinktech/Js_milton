console.log("====pre increment (++value)====")
let a = 10;
console.log(a)
console.log(++a)

console.log("===Post increment (value++)====")

let b = 10;

console.log(b)
console.log(b++)
console.log(b)

console.log("====pre decrement (--value)====")
let c = 10;
console.log(c)
console.log(--c)

console.log("===Post decrement (value--)====")

let d = 10;

console.log(d)
console.log(d--)
console.log(d)