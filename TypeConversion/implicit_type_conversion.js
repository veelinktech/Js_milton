//Type coercion

console.log("5" + 2) //  number 2 automatically converted into string (Type coercion)

console.log("5" - 2) // String 5 is automatically converted into number

console.log(2 + "5") // number 2 automatically converted into string

console.log(2 * "5") // string 5 is converted to number

console.log("10" / 2)// String 10 is converted to number

